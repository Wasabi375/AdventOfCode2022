fn has_dup<T: PartialEq>(slice: &[T]) -> bool {
    for i in 1..slice.len() {
        if slice[i..].contains(&slice[i - 1]) {
            return true;
        }
    }
    false
}

pub fn part_one(input: &str) -> Option<u32> {
    find_start_index(input, 4)
}

fn find_start_index(input: &str, marker_size: u32) -> Option<u32> {
    let mut index = marker_size;
    for window in input.as_bytes().windows(marker_size as usize) {
        if !has_dup(window) {
            return Some(index);
        }
        index += 1;
    }
    None
}

pub fn part_two(input: &str) -> Option<u32> {
    find_start_index(input, 14)
}

fn main() {
    let input = &advent_of_code::read_file("inputs", 6);
    advent_of_code::solve!(1, part_one, input);
    advent_of_code::solve!(2, part_two, input);
}

